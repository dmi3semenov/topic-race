"""
Проверяет русское склонение числительных (функция ``ru_plural``).

Контекст бага: в реальном рендере подзаголовок выводил «761 постов» и
«81 топиков», хотя по правилам русского языка должно быть «761 пост» (число
оканчивается на 1, кроме 11-14) и «81 топик». Эти тесты фиксируют правильное
поведение на всех «ловушечных» граничных случаях:
    • 1, 21, 101, 761        → именительный единственного числа
    • 2..4, 22..24, 762      → родительный единственного («поста»)
    • 5..20, 25, 100, 111    → родительный множественного («постов»)
    • 11..14                 → всегда «постов», даже если последняя цифра 1..4
    • отрицательные          → нормализуются по модулю
"""
from __future__ import annotations

import pytest

from topic_race.render_video import ru_plural


FORMS = ("пост", "поста", "постов")


@pytest.mark.parametrize(
    "n, expected",
    [
        # Ноль и маленькие числа: 0 = много, 1 = один, 2-4 = поста, 5+ = постов
        (0, "постов"),
        (1, "пост"),
        (2, "поста"),
        (3, "поста"),
        (4, "поста"),
        (5, "постов"),
        (10, "постов"),
        # Исключение 11..14 — всегда «постов», даже если последняя цифра 1 или 2
        (11, "постов"),
        (12, "постов"),
        (13, "постов"),
        (14, "постов"),
        # 15..20 — обычные «постов»
        (15, "постов"),
        (20, "постов"),
        # 21..24 — снова повторяется шаблон (последняя цифра решает)
        (21, "пост"),
        (22, "поста"),
        (23, "поста"),
        (24, "поста"),
        (25, "постов"),
        # Сотни: 100 = много, 101 = один, 111 = исключение снова
        (100, "постов"),
        (101, "пост"),
        (111, "постов"),
        (112, "постов"),
        # 761 — тот самый баг из продакшна (оканчивается на 1 → «пост»)
        (761, "пост"),
        (762, "поста"),
        (765, "постов"),
    ],
)
def test_ru_plural_post(n: int, expected: str) -> None:
    assert ru_plural(n, *FORMS) == expected


def test_ru_plural_принимает_отрицательные() -> None:
    # Отрицательные числа нормализуются к модулю — знак не влияет на форму.
    assert ru_plural(-1, *FORMS) == "пост"
    assert ru_plural(-11, *FORMS) == "постов"
    assert ru_plural(-22, *FORMS) == "поста"


def test_ru_plural_на_словах_топик() -> None:
    forms = ("топик", "топика", "топиков")
    assert ru_plural(1, *forms) == "топик"
    assert ru_plural(2, *forms) == "топика"
    assert ru_plural(5, *forms) == "топиков"
    assert ru_plural(81, *forms) == "топик"  # второй реальный баг того же отчёта
